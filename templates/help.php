                    <ol>
                        <li>Možemo da promenimo ili ispravimo naslov ako je potrebno</li>
                        <li>Možemo da ispravimo ime i prezime autora</li>
                        <li>Možemo da pogledamo dokument pre slanja</li>
                        <li>Ako program pronađe više verzija rada ili dodatne fajlove, pojavljuju se strelice pored dugmeta 'Pogledaj dokument'. Tim strelicama
                        možemo da učitamo sledeći fajl dok ne nađemo onaj koji treba proveriti.</li>
                        <li>Ukoliko neki rad nije potrebno slati na proveru na plagijarizam, dovoljno je da ga obrišemo klikom na crveni X u gornjem levom uglu. Ova akcija automatski zapisuje rad u bazu kao proveren i
                        on se neće više pojavljivati u spisku.</li>
                        <li><strong class="vazno">VAŽNO!!! </strong>Ukoliko je neki rad uokviren crvenom bojom, to znači da je taj fajl veći od 20 megabajta, i ne može se slati automatski na proveru.
                        U tom slučaju, potrebno je da skinemo fajl iz aplikacije ili iz Asistenta, da ga obrišemo iz aplikacije i da ga pošaljemo ručno preko iThenticate sajta. Pre brisanja
